CREATE VIEW VW_MI_UW (POLICY_NO, BUSINESS, PLAN_CODE, POLICY_STATUS, PROPOSAL_SIGN_DATE, PROPOSAL_RECEIVE_DATE, UW_EFFECTIVE_DATE, SOURCE, FIRM_NAME, ADVISOR_NAME, OWNER, IS_CLEAN_CASE, AVIVA_TAT, SUSPEND_PERIOD, LOCATION, LIFE_ASSURED, START_TIME, UPLOAD_TIME, PROCESS_NAME, LAST_UNDERWRITER, UNDERWRITER_DECISION_DATE, SENIOR_UW, SENIOR_UW_DECISION_DATE, SOURCE_OF_BUSINESS, TRANSACTION_NAME, END_TIME, LAST_SCREENING_END_TIME, AVIVA_TAT_DAYS, LAST_USER, DECISION, LAST_ACTIVITY_NAME, NO, PROPOSAL_NUMBER, PRODUCT_NAME, STATUS, SUM_ASSURED, TOTAL_PREMIUM, ACTION_TIME, PENDING_REASON, UW_USER_1, UW_USER_2, REISSURANCE, AGENT_NUMBER, AGENT_NAME, DISTRIBUTION_CHANNEL, EXC_PARAM_REGION, EXC_PARAM_PERIOD) AS 
	SELECT 	p."NUMBER" AS POLICY_NO,
	w.LOB AS BUSINESS,
	'N/A' AS PLAN_CODE,
	c.CONTRACTSTATUS AS POLICY_STATUS,
	c.PROPOSALSIGNEDDATE AS PROPOSAL_SIGN_DATE,
	c.PROPOSALRECEIVEDDATE AS PROPOSAL_RECEIVE_DATE,
	'N/A' AS UW_EFFECTIVE_DATE,
	w.SOURCE AS SOURCE,
	c.AGENTNAME AS FIRM_NAME,
	'N/A' AS ADVISOR_NAME,
	c.PROPOSALNAME AS OWNER,
	'' AS IS_CLEAN_CASE,
	'N/A' AS AVIVA_TAT,
	'CALCULATED_FIELD' AS SUSPEND_PERIOD,
	p.REGION_CODE AS LOCATION,
	u.LIFEGIVENNAME AS LIFE_ASSURED,
	w.DATE_CREATED AS START_TIME,
	w.DATE_CREATED AS UPLOAD_TIME,
	'' AS PROCESS_NAME,
	v.MODIFIED_BY AS LAST_UNDERWRITER,
	c1.UWUPDATEDATE AS UNDERWRITER_DECISION_DATE,
	'N/A' AS SENIOR_UW,
	'N/A' AS SENIOR_UW_DECISION_DATE,
	c1.SOB AS SOURCE_OF_BUSINESS,
	w.TRANSACTION_TYPE AS TRANSACTION_NAME,
	'N/A' AS END_TIME,
	'N/A' AS LAST_SCREENING_END_TIME,
	'N/A' AS AVIVA_TAT_DAYS,
	v.MODIFIED_BY AS LAST_USER,
	'N/A' AS DECISION,
	v.TASK_NAME AS LAST_ACTIVITY_NAME,
	'N/A' AS NO,
	c1.APPLICATIONNO AS PROPOSAL_NUMBER,
	'N/A' AS PRODUCT_NAME,
	w.STATUS AS STATUS,
	u.SUMASSURED AS SUM_ASSURED,
	'N/A' AS TOTAL_PREMIUM,
	v.DATE_CREATED AS ACTION_TIME,
	'' AS PENDING_REASON,
	'N/A' AS UW_USER_1,
	'N/A' AS UW_USER_2,
	'N/A' AS REISSURANCE,
	c.COMMISSIONAGENTNO AS AGENT_NUMBER,
	c.AGENTNAME AS AGENT_NAME,
	'N/A' AS DISTRIBUTION_CHANNEL,
	p.REGION_CODE AS EXC_PARAM_REGION,
	v.DATE_CREATED AS EXC_PARAM_PERIOD
	FROM CMDEV.PLANS p
	LEFT JOIN CMDEV.WORKITEMS w ON p."NUMBER" = w.PLAN_ID
	LEFT JOIN CMDEV.VW_QUEUES_TOP v ON w.ID = v.WORKITEM_ID
	LEFT JOIN ODSDEV.CONTRACT@ABC_LINK c ON p."NUMBER" = c.POLICYNO
	LEFT JOIN ODSDEV.CONTRACTSTATUS@ABC_LINK c1 ON c.POLICYNO = c1.POLICYNO
	LEFT JOIN ODSDEV.UWWORKSHEET@ABC_LINK u ON c1.APPLICATIONNO = u.RECEIPT
	WHERE v.TASK_CODE IN ('NB_UW_UW_ACC', 'PS_UW_UW', 'CC_CS_UW');