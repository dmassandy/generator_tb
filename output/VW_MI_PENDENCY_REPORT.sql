CREATE VIEW VW_MI_PENDENCY_REPORT (NO, POLICY_NO, POLICY_HOLDER_NAME, AGENT_NAME, AGENT_CODE, BRANCHREGION, SOURCE, REQUEST_TYPE, REQUEST_RECEIVING_DATE, PROCESS_STATUS, STANDARD_TAT, ACTUAL_PROCESS_TIME, OVER_TAT_DAY, NOTE) AS 
	SELECT 	'N/A' AS NO,
	p.NUMBER AS POLICY_NO,
	u.LIFEGIVENNAME AS POLICY_HOLDER_NAME,
	c.AGENTNAME AS AGENT_NAME,
	c.COMMISSIONAGENTNO AS AGENT_CODE,
	c.CONTRACTBRANCH AS BRANCHREGION,
	w.SOURCE AS SOURCE,
	w.TRANSACTION_TYPE AS REQUEST_TYPE,
	c.PROPOSALRECEIVEDDATE AS REQUEST_RECEIVING_DATE,
	w.STATUS AS PROCESS_STATUS,
	'N/A' AS STANDARD_TAT,
	'CALCULATED_FIELD' AS ACTUAL_PROCESS_TIME,
	'CALCULATED_FIELD' AS OVER_TAT_DAY,
	d.NOTES AS NOTE
	FROM 	ODSDEV.UWWORKSHEET@ABC_LINK u,
	CMDEV.WORKITEMS w,
	CMDEV.PLANS p,
	ODSDEV.CONTRACT@ABC_LINK c,
	CMDEV.DESCRIPTION d;