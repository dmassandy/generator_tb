CREATE VIEW VW_MI_NB_PENDENCY_REPORT (POLICY_NO, SOURCE, LOCATION, BUSINESS, PLAN_CODE, ADVISOR_NAME, FIRM_NAME, OWNER, LIFE_ASSURED, POLICY_STATUS, START_TIME, UPLOAD_TIME, PROPOSAL_SIGN_DATE, PROPOSAL_RECEIVE_DATE, LAST_ACTIVITY, LAST_USER, USER_ACTION_DATE, NTU_DATE, RESUME_DATE, REASON, PEND_PERIOD, AGEING, NO, PROPOSAL_NUMBER, PRODUCT_NAME, STATUS_UW, PENDING_CODE_STATUSR_OR_W, SUM_ASSURED, TOTAL_PREMIUM, AGENT_NUMBER, AGENT_NAME, DISTRIBUTION_CHANNEL, STATUS, REMAIN_TIME, EXC_PARAM_REGION, EXC_PARAM_PERIOD) AS 
	SELECT 	p.NUMBER AS POLICY_NO,
	w.SOURCE AS SOURCE,
	p.REGION_CODE AS LOCATION,
	w.LOB AS BUSINESS,
	'N/A' AS PLAN_CODE,
	'N/A' AS ADVISOR_NAME,
	c.AGENTNAME AS FIRM_NAME,
	c.PROPOSALNAME AS OWNER,
	u.LIFEGIVENNAME AS LIFE_ASSURED,
	c.CONTRACTSTATUS AS POLICY_STATUS,
	w.DATE_CREATED AS START_TIME,
	w.DATE_CREATED AS UPLOAD_TIME,
	c.PROPOSALSIGNEDDATE AS PROPOSAL_SIGN_DATE,
	c.PROPOSALRECEIVEDDATE AS PROPOSAL_RECEIVE_DATE,
	v.TASK_NAME AS LAST_ACTIVITY,
	v.MODIFIED_BY AS LAST_USER,
	v.DATE_MODIFIED AS USER_ACTION_DATE,
	v.NTU_DATE AS NTU_DATE,
	'CALCULATED_FIELD' AS RESUME_DATE,
	'BLANK - FILL IN' AS REASON,
	'CALCULATED_FIELD' AS PEND_PERIOD,
	'CALCULATED_FIELD' AS AGEING,
	'N/A' AS NO,
	c1.APPLICATIONNO AS PROPOSAL_NUMBER,
	'N/A' AS PRODUCT_NAME,
	v.STATUS AS STATUS_UW,
	'N/A' AS PENDING_CODE_STATUSR_OR_W,
	u.SUMASSURED AS SUM_ASSURED,
	u.SINGLEPREMIUM AS TOTAL_PREMIUM,
	c.COMMISSIONAGENTNO AS AGENT_NUMBER,
	c.AGENTNAME AS AGENT_NAME,
	'N/A' AS DISTRIBUTION_CHANNEL,
	w.STATUS AS STATUS,
	'N/A' AS REMAIN_TIME,
	p.REGION_CODE AS EXC_PARAM_REGION,
	v.DATE_CREATED AS EXC_PARAM_PERIOD
	FROM 	ODSDEV.UWWORKSHEET@ABC_LINK u,
	ODSDEV.CONTRACT_STATUS@ABC_LINK c1,
	CMDEV.PLANS p,
	ODSDEV.CONTRACT@ABC_LINK c,
	CMDEV.WORKITEMS w,
	CMDEV.VW_QUEUES_TOP v;